# What are the benefits of using 
# Object Oriented Programming in Ruby? Think of as many as you can.

=begin
1) Parts of the OO programs are able to interact with each other, reducing
dependencies and facilitating maintenance.
2) Allows us to only expose functionality to the parts of the code that require
it, so issues like namespacing are rarer.
2) OOP allows us to think at a higher level of abstraction creating classes
according to problem domains and using real-life nouns to represent objects.
3) By defining classes and using concepts such as inheritance,
we are introducing more detailed behaviours that provide greater reusability and
flexibility.
=end